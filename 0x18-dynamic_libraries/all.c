#include <stdio.h>
/**
*add - add two integers
*@a: first int
*@b: second int
*Return: addition
*/
int add(int a, int b)
{
return (a + b);
}
/**
*sub - substract two integers
*@a: first integer
*@b: second integer
*Return: substract final result
*/
int sub(int a, int b)
{
return (a - b);
}

/**
*div - divides two integers
*@a: first integer
*@b: second integer
*return: division
*/
int div(int a, int b)
{
return (a / b);
}

/**
*mul - multiplys two integers
*@a: first integer
*@b: second integer
*return: multiplication
*/
int mul(int a, int b)
{
return (a * b);
}

/**
*mod - modulus of a function
*@a: first integer
*@b: second integer
*return: modulus
*/
int mod(int a, int b)
{
return (a % b);
}
